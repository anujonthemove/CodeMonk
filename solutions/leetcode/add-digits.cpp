using namespace std;

class Solution {
  public:
    int sum(int num) {
        int res = 0;
        while (num > 0) {
            res += num % 10;
            num /= 10;
        }
        return res;
    }
    int addDigits(int num) {
        while (num >= 10) {
            num = sum(num);
        }
        return num;
    }
};

